%% 子函数――训练数据归一化，弃用
function x = normalize(x, mu, sigma)   % 将训练数据的平均值与标准差传递过来，对测试数据进行归一化
    x=bsxfun(@minus,x,mu);
	x=bsxfun(@rdivide,x,sigma);
end
